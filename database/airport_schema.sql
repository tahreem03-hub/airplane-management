-- PROCEDURE 1: Add New Flight
CREATE OR REPLACE PROCEDURE ADD_NEW_FLIGHT (
    p_Flight_ID IN NUMBER,
    p_Destination IN VARCHAR2,
    p_Departure_Time IN DATE,
    p_Arrival_Time IN DATE,
    p_Airline_ID IN NUMBER,
    p_Aircraft_ID IN NUMBER,
    p_Gate_ID IN NUMBER
) IS
BEGIN
    INSERT INTO FLIGHT (Flight_ID, Status, Destination, Departure_Time, Arrival_Time, Airline_ID, Aircraft_ID, Gate_ID)
    VALUES (p_Flight_ID, 'Scheduled', p_Destination, p_Departure_Time, p_Arrival_Time, p_Airline_ID, p_Aircraft_ID, p_Gate_ID);
    
    UPDATE GATE SET Status = 'Occupied' WHERE Gate_ID = p_Gate_ID;
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Flight ' || p_Flight_ID || ' added successfully');
END;
/

-- PROCEDURE 2: Cancel Booking
CREATE OR REPLACE PROCEDURE CANCEL_BOOKING (
    p_Booking_ID IN NUMBER
) IS
BEGIN
    UPDATE FLIGHT_PASSENGER SET Status = 'Cancelled' WHERE Booking_ID = p_Booking_ID;
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Booking ' || p_Booking_ID || ' cancelled');
END;
/

-- PROCEDURE 3: Update Flight Status
CREATE OR REPLACE PROCEDURE UPDATE_FLIGHT_STATUS (
    p_Flight_ID IN NUMBER,
    p_New_Status IN VARCHAR2
) IS
    v_Gate_ID NUMBER;
BEGIN
    SELECT Gate_ID INTO v_Gate_ID FROM FLIGHT WHERE Flight_ID = p_Flight_ID;
    UPDATE FLIGHT SET Status = p_New_Status WHERE Flight_ID = p_Flight_ID;
    
    IF p_New_Status = 'Arrived' THEN
        UPDATE GATE SET Status = 'Available' WHERE Gate_ID = v_Gate_ID;
    END IF;
    COMMIT;
END;
/

-- TRIGGER 

-- TRIGGER: Auto-update gate status when flight is assigned
CREATE OR REPLACE TRIGGER TRG_UPDATE_GATE
AFTER INSERT ON FLIGHT
FOR EACH ROW
BEGIN
    UPDATE GATE SET Status = 'Occupied' WHERE Gate_ID = :NEW.Gate_ID;
END;
/

-- TRIGGER: Log flight status changes
CREATE TABLE FLIGHT_LOG (
    Log_ID NUMBER PRIMARY KEY,
    Flight_ID NUMBER,
    Old_Status VARCHAR2(20),
    New_Status VARCHAR2(20),
    Change_Date DATE DEFAULT SYSDATE
);

CREATE SEQUENCE flight_log_seq;

CREATE OR REPLACE TRIGGER TRG_LOG_FLIGHT_STATUS
AFTER UPDATE OF Status ON FLIGHT
FOR EACH ROW
BEGIN
    INSERT INTO FLIGHT_LOG (Log_ID, Flight_ID, Old_Status, New_Status)
    VALUES (flight_log_seq.NEXTVAL, :OLD.Flight_ID, :OLD.Status, :NEW.Status);
END;
/

-- VIEW 

-- VIEW 1: Flight Schedule View
CREATE OR REPLACE VIEW FLIGHT_SCHEDULE_VIEW AS
SELECT 
    f.Flight_ID,
    f.Destination,
    f.Departure_Time,
    f.Status,
    a.Name AS Airline_Name,
    ac.Model AS Aircraft_Model,
    g.Gate_ID
FROM FLIGHT f
JOIN AIRLINE a ON f.Airline_ID = a.Airline_ID
JOIN AIRCRAFT ac ON f.Aircraft_ID = ac.Aircraft_ID
JOIN GATE g ON f.Gate_ID = g.Gate_ID;

-- VIEW 2: Passenger Booking Summary
CREATE OR REPLACE VIEW PASSENGER_BOOKING_VIEW AS
SELECT 
    p.Passenger_ID,
    p.Name,
    COUNT(b.Booking_ID) AS Total_Bookings,
    MAX(b.Booking_Date) AS Last_Booking
FROM PASSENGER p
LEFT JOIN FLIGHT_PASSENGER b ON p.Passenger_ID = b.Passenger_ID
GROUP BY p.Passenger_ID, p.Name;

