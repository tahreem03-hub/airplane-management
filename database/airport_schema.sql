-- TABLE 1: AIRPORT

CREATE TABLE AIRPORT (
    Airport_ID     NUMBER PRIMARY KEY,
    Name           VARCHAR2(100) NOT NULL,
    City           VARCHAR2(50) NOT NULL,
    Country        VARCHAR2(50) NOT NULL,
    Capacity       NUMBER
);


-- TABLE 2: TERMINAL

CREATE TABLE TERMINAL (
    Terminal_ID     NUMBER PRIMARY KEY,
    Terminal_Name   VARCHAR2(50) NOT NULL,
    Capacity        NUMBER,
    Airport_ID      NUMBER NOT NULL,
    FOREIGN KEY (Airport_ID) REFERENCES AIRPORT(Airport_ID) ON DELETE CASCADE
);

-- TABLE 3: GATE
CREATE TABLE GATE (
    Gate_ID      NUMBER PRIMARY KEY,
    Gate_Type    VARCHAR2(20),
    Status       VARCHAR2(20) DEFAULT 'Available',
    Terminal_ID  NUMBER NOT NULL,
    FOREIGN KEY (Terminal_ID) REFERENCES TERMINAL(Terminal_ID) ON DELETE CASCADE
);

-- TABLE 4: AIRLINE
CREATE TABLE AIRLINE (
    Airline_ID   NUMBER PRIMARY KEY,
    Name         VARCHAR2(100) NOT NULL,
    IATA_Code    VARCHAR2(3) UNIQUE NOT NULL,
    Status       VARCHAR2(20) DEFAULT 'Active',
    Airline_Type VARCHAR2(20)
);

-- TABLE 5: AIRCRAFT
CREATE TABLE AIRCRAFT (
    Aircraft_ID     NUMBER PRIMARY KEY,
    Model           VARCHAR2(50) NOT NULL,
    Capacity        NUMBER,
    Registration_No VARCHAR2(20) UNIQUE NOT NULL,
    Status          VARCHAR2(20) DEFAULT 'Active'
);

-- TABLE 6: FLIGHT
CREATE TABLE FLIGHT (
    Flight_ID       NUMBER PRIMARY KEY,
    Status          VARCHAR2(20) DEFAULT 'Scheduled',
    Destination     VARCHAR2(100) NOT NULL,
    Departure_Time  DATE NOT NULL,
    Arrival_Time    DATE NOT NULL,
    Airline_ID      NUMBER NOT NULL,
    Aircraft_ID     NUMBER NOT NULL,
    Gate_ID         NUMBER NOT NULL,
    FOREIGN KEY (Airline_ID) REFERENCES AIRLINE(Airline_ID),
    FOREIGN KEY (Aircraft_ID) REFERENCES AIRCRAFT(Aircraft_ID),
    FOREIGN KEY (Gate_ID) REFERENCES GATE(Gate_ID)
);

-- TABLE 7: PASSENGER
CREATE TABLE PASSENGER (
    Passenger_ID    NUMBER PRIMARY KEY,
    Name            VARCHAR2(100) NOT NULL,
    Passport_No     VARCHAR2(20) UNIQUE NOT NULL,
    Nationality     VARCHAR2(50) NOT NULL,
    DOB             DATE,
    Contact_Number  VARCHAR2(15),
    Email           VARCHAR2(100)
);

-- TABLE 8: FLIGHT_PASSENGER (Booking)
CREATE TABLE FLIGHT_PASSENGER (
    Booking_ID      NUMBER PRIMARY KEY,
    Flight_ID       NUMBER NOT NULL,
    Passenger_ID    NUMBER NOT NULL,
    Booking_Date    DATE DEFAULT SYSDATE,
    Seat_Number     VARCHAR2(5),
    Status          VARCHAR2(20) DEFAULT 'Confirmed',
    FOREIGN KEY (Flight_ID) REFERENCES FLIGHT(Flight_ID),
    FOREIGN KEY (Passenger_ID) REFERENCES PASSENGER(Passenger_ID)
);

-- TABLE 9: BOARDING_PASS
CREATE TABLE BOARDING_PASS (
    Boarding_ID     NUMBER PRIMARY KEY,
    Seat_No         VARCHAR2(5) NOT NULL,
    Gate_Code       VARCHAR2(10) NOT NULL,
    Boarding_Time   DATE NOT NULL,
    Booking_ID      NUMBER NOT NULL,
    FOREIGN KEY (Booking_ID) REFERENCES FLIGHT_PASSENGER(Booking_ID)
);

-- TABLE 10: BAGGAGE
CREATE TABLE BAGGAGE (
    Baggage_ID      NUMBER PRIMARY KEY,
    Weight          NUMBER,
    Tag_Number      VARCHAR2(50) UNIQUE NOT NULL,
    Baggage_Type    VARCHAR2(20),
    Booking_ID      NUMBER NOT NULL,
    FOREIGN KEY (Booking_ID) REFERENCES FLIGHT_PASSENGER(Booking_ID)
);

-- TABLE 11: SECURITY_CHECKPOINT
CREATE TABLE SECURITY_CHECKPOINT (
    Checkpoint_ID   NUMBER PRIMARY KEY,
    Checkpoint_Name VARCHAR2(100) NOT NULL,
    Location        VARCHAR2(100) NOT NULL,
    Is_Operational  CHAR(1) DEFAULT 'Y'
);

-- TABLE 12: SECURITY_CHECK
CREATE TABLE SECURITY_CHECK (
    Check_ID        NUMBER PRIMARY KEY,
    Check_Type      VARCHAR2(20),
    Check_Time      DATE DEFAULT SYSDATE,
    Result          VARCHAR2(20),
    Passenger_ID    NUMBER NOT NULL,
    Checkpoint_ID   NUMBER NOT NULL,
    FOREIGN KEY (Passenger_ID) REFERENCES PASSENGER(Passenger_ID),
    FOREIGN KEY (Checkpoint_ID) REFERENCES SECURITY_CHECKPOINT(Checkpoint_ID)
);

-- TABLE 13: CREW
CREATE TABLE CREW (
    Employee_ID     NUMBER PRIMARY KEY,
    Name            VARCHAR2(100) NOT NULL,
    Role            VARCHAR2(50) NOT NULL,
    Contact         VARCHAR2(15),
    Hire_Date       DATE DEFAULT SYSDATE,
    Crew_Type       VARCHAR2(20)
);

-- TABLE 14: PILOT (Subclass)
CREATE TABLE PILOT (
    Pilot_ID        NUMBER PRIMARY KEY,
    License_No      VARCHAR2(20) UNIQUE NOT NULL,
    Flight_Hours    NUMBER DEFAULT 0,
    FOREIGN KEY (Pilot_ID) REFERENCES CREW(Employee_ID) ON DELETE CASCADE
);

-- TABLE 15: FLIGHT_CREW
CREATE TABLE FLIGHT_CREW (
    Assignment_ID   NUMBER PRIMARY KEY,
    Flight_ID       NUMBER NOT NULL,
    Employee_ID     NUMBER NOT NULL,
    Role_On_Flight  VARCHAR2(50),
    FOREIGN KEY (Flight_ID) REFERENCES FLIGHT(Flight_ID),
    FOREIGN KEY (Employee_ID) REFERENCES CREW(Employee_ID)
);

-- TABLE 16: FREQUENT_FLYER (Subclass)
CREATE TABLE FREQUENT_FLYER (
    FF_ID           NUMBER PRIMARY KEY,
    Miles_Earned    NUMBER DEFAULT 0,
    Tier_Level      VARCHAR2(20) DEFAULT 'Basic',
    Passenger_ID    NUMBER NOT NULL UNIQUE,
    FOREIGN KEY (Passenger_ID) REFERENCES PASSENGER(Passenger_ID)
);

-- TABLE 17: TRANSFER_PASSENGER (Subclass)
CREATE TABLE TRANSFER_PASSENGER (
    Transfer_ID        NUMBER PRIMARY KEY,
    Transit_Airport    VARCHAR2(100) NOT NULL,
    Layover_Duration   NUMBER,
    Passenger_ID       NUMBER NOT NULL UNIQUE,
    FOREIGN KEY (Passenger_ID) REFERENCES PASSENGER(Passenger_ID)
);

